    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed

        Customer customer = new Customer(txtID.getText(),
                txtName.getText(), txtAddress.getText(), Double.parseDouble(txtSalary.getText()));

        boolean result = CustomerController.updateCustomer(customer);

        if (result) {
            JOptionPane.showMessageDialog(this, "Customer Updated Sucessfully");
        } else {
            JOptionPane.showMessageDialog(this, "Sometihing went wrong");
        }

    }//GEN-LAST:event_btnUpdateActionPerformed

