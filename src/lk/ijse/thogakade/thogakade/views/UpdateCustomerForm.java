    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed

        Customer customer = new Customer(txtID.getText(),
                txtName.getText(), txtAddress.getText(), 0.0);

        boolean result = CustomerController.deleteCustomer(customer);

        if (result) {
            JOptionPane.showMessageDialog(this, "Customer Delete Sucessfully");
        } else {
            JOptionPane.showMessageDialog(this, "Sometihing went wrong");
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

